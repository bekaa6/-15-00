from abc import ABC, abstractmethod


# 1. Абстрактілі класс Vehicle құру
class Vehicle(ABC):
    """
    Көлік құралдарының абстрактілі негізгі класы.
    Барлық подкластар орындауға міндетті келісімшартты (абстрактілі әдістерді) анықтайды.
    """

    # 1.1. Абстрактілі әдіс move()
    @abstractmethod
    def move(self):
        """Көлік құралының қалай қозғалатынын анықтайды."""
        pass

    # 1.2. Абстрактілі әдіс fuel_type()
    @abstractmethod
    def fuel_type(self):
        """Көлік құралының отын/энергия түрін анықтайды."""
        pass


# 2. Subclass Car құру және іске асыру
class Car(Vehicle):
    """
    Vehicle-дің нақты подкласы.
    Vehicle-де анықталған барлық абстрактілі әдістерді іске асыруы керек.
    """

    # move() абстрактілі әдісін іске асыру
    def move(self):
        # Автокөлік төрт доңғалақпен жүреді
        print("Автокөлік жолда жүріп келеді.")

    # fuel_type() абстрактілі әдісін іске асыру
    def fuel_type(self):
        # Автокөлік бензин/мұнай пайдаланады
        print("Отын түрі: Бензин/Мұнай.")


# 3. Subclass Bicycle құру және іске асыру
class Bicycle(Vehicle):
    """
    Vehicle-дің нақты подкласы.
    Vehicle-де анықталған барлық абстрактілі әдістерді іске асыруы керек.
    """

    # move() абстрактілі әдісін іске асыру
    def move(self):
        # Велосипедті адам педальмен қозғалтады
        print("Велосипед адам күшімен педаль арқылы қозғалады.")

    # fuel_type() абстрактілі әдісін іске асыру
    def fuel_type(self):
        # Велосипед адам күшін пайдаланады
        print("Отын түрі: Адам күші/Бұлшық ет.")


# --- Класстарды тексеру ---

print("--- Car (Автокөлік) тестілеу ---")
my_car = Car()
my_car.move()
my_car.fuel_type()
print("-" * 30)

print("--- Bicycle (Велосипед) тестілеу ---")
my_bicycle = Bicycle()
my_bicycle.move()
my_bicycle.fuel_type()
print("-" * 30)

